package MyOperator;

public class MyInc {


    public static void main(String[] args) {
        int a=10;
        int b=a++; //Post Increment

        int x = 121;
        int y = x++; //Post Increment

        int m = 100;
        int n = ++m;

        System.out.println(a);
        System.out.println(b);


        System.out.println(x);
        System.out.println(y);

        System.out.println(m);
        System.out.println(n);

    }

}
